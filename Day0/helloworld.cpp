#include<iostream> // including header file

using namespace std; // specifying the namespace to look for names(or variables, functions etc.)

int main() { // the main function, starting point of execution of any program
    cout<<"Hello world!"<<endl; // printing 'Hello world!' to the console
    return 0; // returning 0 as exit status to say it's a successful execution
}

/*
- Basically if exit status is zero(0), it means program/binary execution succeeded.
And if the exit status is non-zero(-2, -99 etc), it mean program/binary execution failed.
*/